# Вечным двигателем считается тот, у которого коэффициент полезного действия не меньше 100%. Зная работу, совершаемую двигателем за один цикл, и количество теплоты, полученное от нагревателя, можно рассчитать КПД тепловой машины в долях от единицы по формуле:
