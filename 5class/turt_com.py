import turtle #добавить черепашку
turtle.speed(1) #скорость
turtle.shape('turtle') #форма черепашки
turtle.color('red') #цвет
turtle.penup() #поднять ручку
turtle.pendown() #опустить ручку
turtle.forward(50) #вперед на 50
turtle.backward(50) #назад на 50
turtle.right(90) #направо на 90
turtle.left(90) #налево на 90