import turtle as t #добавить черепашку, назвать ее t.
t.speed(1) #скорость
t.shape('turtle') #форма черепашки
t.color('red') #цвет

t.penup() #поднять ручку
t.pendown() #опустить ручку

t.forward(50) #вперед на 50
t.fd(90) # То же самое
t.backward(50) #назад на 50
t.bk(90) # То же самое
t.right(90) #направо на 90
t.rt(90) # То же самое
t.left(90) #налево на 90
t.lt(90) # То же самое

import time #добавить библиотку "время"
time.sleep(10) #остановить програму на 10 сеунд